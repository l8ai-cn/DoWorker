import type {
  LoopAIMessages,
  LoopMessageTranslator,
} from "./loop-workbench-message-types";

export function createLoopAIMessages(t: LoopMessageTranslator): LoopAIMessages {
  return {
    toolbar: t("ai.toolbar"),
    generateMode: t("ai.generateMode"),
    explainMode: t("ai.explainMode"),
    generateTitle: t("ai.generateTitle"),
    generateDescription: t("ai.generateDescription"),
    explainTitle: t("ai.explainTitle"),
    explainDescription: t("ai.explainDescription"),
    resource: t("ai.resource"),
    resourcePlaceholder: t("ai.resourcePlaceholder"),
    prompt: t("ai.prompt"),
    promptPlaceholder: t("ai.promptPlaceholder"),
    loadingResources: t("ai.loadingResources"),
    resourceError: t("ai.resourceError"),
    retry: t("ai.retry"),
    noResources: t("ai.noResources"),
    generate: t("ai.generate"),
    generating: t("ai.generating"),
    generationError: t("ai.generationError"),
    unchanged: t("ai.unchanged"),
    stale: t("ai.stale"),
    current: t("ai.current"),
    proposed: t("ai.proposed"),
    cancel: t("ai.cancel"),
    close: t("ai.close"),
    back: t("ai.back"),
    confirm: t("ai.confirm"),
    projection: {
      title: t("ai.projection.title"),
      unavailable: t("ai.projection.unavailable"),
      overview: t("ai.projection.overview"),
      schemaVersion: t("ai.projection.schemaVersion"),
      loopName: t("ai.projection.loopName"),
      limits: t("ai.projection.limits"),
      iterations: t("ai.projection.iterations"),
      tokens: t("ai.projection.tokens"),
      timeout: t("ai.projection.timeout"),
      noProgress: t("ai.projection.noProgress"),
      sameError: t("ai.projection.sameError"),
      repeat: t("ai.projection.repeat"),
      repeatName: t("ai.projection.repeatName"),
      repeatMax: t("ai.projection.repeatMax"),
      until: t("ai.projection.until"),
      agent: t("ai.projection.agent"),
      agentName: t("ai.projection.agentName"),
      agentPrompt: t("ai.projection.agentPrompt"),
      verifier: t("ai.projection.verifier"),
      verifierName: t("ai.projection.verifierName"),
      verifierCommand: t("ai.projection.verifierCommand"),
      verifierAccept: t("ai.projection.verifierAccept"),
      failure: t("ai.projection.failure"),
      failurePolicy: t("ai.projection.failurePolicy"),
      empty: t("ai.projection.empty"),
      iterationsValue: (value) => t("ai.projection.iterationsValue", { value }),
      tokensValue: (value) => t("ai.projection.tokensValue", { value }),
      minutesValue: (value) => t("ai.projection.minutesValue", { value }),
      timesValue: (value) => t("ai.projection.timesValue", { value }),
      failurePolicyLabel: (value) => {
        if (value === "pause") return t("ai.projection.failurePause");
        if (value === "fail") return t("ai.projection.failureFail");
        return value || t("ai.projection.empty");
      },
    },
    repair: {
      title: t("ai.repair.title"),
      description: t("ai.repair.description"),
      diagnostic: t("ai.repair.diagnostic"),
      field: t("ai.repair.field"),
      prompt: t("ai.repair.prompt"),
      promptPlaceholder: t("ai.repair.promptPlaceholder"),
      repair: t("ai.repair.repair"),
      repairing: t("ai.repair.repairing"),
      error: t("ai.repair.error"),
      patch: t("ai.repair.patch"),
      patchValue: (oldValue, newValue) =>
        t("ai.repair.patchValue", { oldValue, newValue }),
    },
  };
}
