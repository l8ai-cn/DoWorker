const { chromium } = require('@playwright/test');
const BASE = 'http://localhost:10007';
(async () => {
  const browser = await chromium.launch();
  const page = await browser.newPage({ viewport:{ width:1440, height:900 } });
  const errs=[]; page.on('pageerror',e=>errs.push(e.message));
  const caps=[]; page.on('response',r=>{ if(r.url().includes('/coordinator/projects')) caps.push(`${r.request().method()} ${r.status()}`); });
  await page.goto(`${BASE}/login`,{waitUntil:'domcontentloaded'}); await page.waitForTimeout(2000);
  await page.fill('#username','dev@agentsmesh.local'); await page.fill('#password','AdminAb123456');
  await page.click('button:has-text("SIGN IN")');
  await page.waitForURL(u=>!u.toString().includes('/login'),{timeout:30000}).catch(()=>{});
  await page.waitForTimeout(2500);
  await page.goto(`${BASE}/dev-org/automation`,{waitUntil:'domcontentloaded'}); await page.waitForTimeout(2500);

  await page.locator('button:has-text("New project")').first().click();
  await page.waitForTimeout(1000);
  const uniq='e2e-auto-'+Date.now().toString().slice(-6);
  await page.getByPlaceholder('e.g. Triage bot').fill(uniq);
  await page.getByText('Select a repository').click();
  await page.waitForTimeout(800);
  await page.getByText('Demo API', { exact:true }).click();
  await page.waitForTimeout(500);
  const createBtn = page.locator('button:has-text("Create")').last();
  console.log('create disabled =', await createBtn.isDisabled());
  await createBtn.click();
  await page.waitForTimeout(4500);
  const txt = await page.locator('body').innerText();
  console.log('project card visible =', txt.includes(uniq));
  await page.screenshot({ path:'output/playwright/06-after-create.png', fullPage:true });
  console.log('API calls:', caps.join(' | '));
  console.log('pageErrors:', errs.slice(0,5).join(' || ')||'(none)');
  console.log('NAME='+uniq);
  await browser.close();
})().catch(e=>{console.error('FAIL',e.stack);process.exit(1)});
